    private void btnRegistrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarseActionPerformed
        if(txtNumCuenta.getText().length() == 7){
            try {
                Estudiante e = new Estudiante(
                        new Nombre(txtNombre.getText(), txtApellido.getText(), true),
                        new Correo(txtCorreo.getText()),
                        txtNumCuenta.getText(),
                        String.copyValueOf(txtPassword.getPassword()),
                        (String) cbxPlanEstudios.getSelectedItem());
                app.estudiantes.add(e);
                Archivos.guardarArchivo("estudiantes.dat", app.estudiantes);
                System.out.println("Estudiante guardado");
                
            } catch (Exception ex) {
                System.out.println("hubo un error");
            }
        }else{
            System.out.println("El numero de cuenta es incorrecto");
        }
        
    }//GEN-LAST:event_btnRegistrarseActionPerformed

  
